
#include <iostream>
#include <cmath>
#include <fstream>
#include <vector>

using namespace std;

//************************************************************************************************************

int main(){

ofstream outfile1("outputA.txt");
ofstream outfile2("outputC.txt");

float u1, u2, r12, r21, p1, p2, T; 

//************************************************************************************************************

u1 = 2.0;
u2 = 3.0;

//************************************************************************************************************

	for (T = 0.0; T <= 20.0; T = T + 0.05){

	r12 = 1.0 / (1.0 + exp(- (u2 - u1) / T));
	r21 = 1.0 / (1.0 + exp(- (u1 - u2) / T));

	p1 = r21 / (r12 + r21);
	p2 = r12 / (r12 + r21);

	outfile1 << T << '\t' << p1 << '\t' << p2 << endl;
	outfile2 << T << '\t' << p1*p1 << '\t' << p1*p2 << '\t' << p1*p2 << '\t' << p2*p2 << endl;
	}

//************************************************************************************************************

outfile1.close();
outfile2.close();

return 0;

}

